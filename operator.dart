void main() {
  // operator aritmatika ( + - * / %)
  // incrmenet dan decrement
  var nilai = 1;
  print(++nilai);

  // perbandingan ( >, <, >=, <=, ==, !=)
  var harga = 10;
  var menawar = 5;

  if (harga == menawar) {
    print("terlalu murah");
  } else if (harga < menawar) {
    print("masih terlalu murah");
  } else if (harga > menawar) {
    print("selamat anda bisa membeli");
  }
}
